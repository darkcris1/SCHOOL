public class Activity2 {
    public static void main(String[] args) {
        char c = 'C';
        char r = 'R';
        char i = 'I';
        char s = 'S';

        System.out.println(c + "" + r+ "" + i + "" + s + "");

        int x = 5;
        int xy = 3;
        
        int m = 7;
        int mn = 4;
        
        double t = 20;
        double tu = 7;
        
        float y = 11;
        float yz = 3;

        // add 
        System.out.println(x + "+" + xy + "=" + (x+ xy));

        // sub
        System.out.println(m + "-" + mn + "=" + (m - mn));

        // times
        System.out.println(t + "*" + tu + "=" + (t * tu));

        // div
        System.out.println(y + "/" + yz + "=" + (y / yz));
    }   
}
